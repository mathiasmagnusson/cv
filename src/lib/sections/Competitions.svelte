<section>
    <h2>Tävlingar</h2>

    <h3>Programmeringsolympiaden 2018</h3>
    <p>
        <a href="https://www.progolymp.se/">Programmeringsolympiaden</a> är
        en tävling i programmeringsproblemlösning som hålls av
        <a href="https://www.kodsport.se">Kodsport Sverige</a>. I tävlingen
        deltog jag i skolkvalet. Jag inte gick vidare till finalen, men fick
        mest poäng i min skola.
    </p>

    <h3>Programmeringsolympiaden 2019</h3>
    <p>
        Året efter deltog jag i skolkvalet igen. Jag fick återigen mest
        poäng i skolan och dessutom så gick jag vidare till sverigefinalen
        som hölls i Googles högkvarter i Stockholm.
    </p>

    <h3>Programmeringsolympiaden 2020</h3>
    <p>
        Denna gång deltog jag i både skol- och onlinekvalet. Jag kvalade
        vidare till finalen genom onlinekvalet. Finalen hölls på KTH och jag
        kom på 11:e plats.
    </p>

    <h3>Säkerhetssm 2019</h3>
    <p>
        <a href="https://sakerhetssm.se/">Säkerhetssm</a> är en Capture The
        Flag-tävling inom IT-säkerhet. Jag och två vänner från skolan
        tävlade tillsammans under lagnamnet Bumbibjörnarna🐻 och kom på
        tredje plats av 49 tävlande. Tävlingen hölls online.
    </p>

    <h3>Säkerhetssm 2020</h3>
    <p>
        Året efter deltog vi igen under samma namn och kom på fjärde plats av
        44 tävlande. Detta gav oss en plats i finalen av Midnight Sun CTF 2020.
    </p>

    <h3>Midnight Sun CTF 2020</h3>
    <p>
        <a href="https://2020.midnightsunctf.se/">Midnight Sun CTF</a> har
        upplägg mycket likt Säkerhetssm. Den hölls däremot på plats i Posthuset,
        Stockholm. Där tävlade två av oss i Bumbibjörnarna🐻 tillsammans med
        laget som kom på tredje plats i Säkerhetssm 2020.
    </p>

    <h3>Midnight Sun CTF 2021</h3>
    <p>
        Året efter kvalade vi i Bumbibjörnarna🐻 och tre av våra vänner från
        gymnasiet in till finalen via den "vanliga" kvalificeringstävlingen.
    </p>

    <h3>Google Hash Code 2021</h3>
    <p>
        <a href="https://hashcode.withgoogle.com">Google Hash Code</a> är en
        programmeringstävling som Google håller online varje år för deltagare i
        hela världen. Jag tävlade med två vänner från KTH under namnet
        DuckDuckRust🦆🦆🦀 och i kvalificeringsomgången kom vi på plats 1150 av
        totalt ca 9000 deltagande lag.
    </p>
</section>

